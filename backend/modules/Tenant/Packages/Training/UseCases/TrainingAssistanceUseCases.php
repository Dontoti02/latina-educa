<?php

namespace Modules\Tenant\Packages\Training\UseCases;

use Exception;
use Illuminate\Http\Request;
use Illuminate\Support\Facades\DB;
use Modules\Shared\Utils\Response;
use Modules\Tenant\Packages\Training\Repositories\TrainingAssistanceRepository;

class TrainingAssistanceUseCases
{
    public static function dates(int $training_id)
    {
        try {
            $result = TrainingAssistanceRepository::dates($training_id);
            return Response::success($result);
        } catch (Exception $e) {
            return Response::error($e->getMessage());
        }
    }

    public static function list(Request $request)
    {
        try {
            $result = TrainingAssistanceRepository::list($request);
            return Response::success($result);
        } catch (Exception $e) {
            return Response::error($e->getMessage());
        }
    }

    public static function create(Request $request)
    {
        DB::beginTransaction();
        try {
            $result = TrainingAssistanceRepository::create($request);
            DB::commit();
            return Response::success($result);
        } catch (Exception $e) {
            DB::rollBack();
            return Response::error($e->getMessage());
        }
    }

    public static function mark(int $id, Request $request)
    {
        DB::beginTransaction();
        try {
            $result = TrainingAssistanceRepository::mark($id, $request);
            DB::commit();
            return Response::success($result);
        } catch (Exception $e) {
            DB::rollBack();
            return Response::error($e->getMessage());
        }
    }
    public static function deleteMultiple(Request $request)
    {
        DB::beginTransaction();
        try {
            $result = TrainingAssistanceRepository::deleteMultiple($request);
            DB::commit();
            return Response::success($result);
        } catch (Exception $e) {
            DB::rollBack();
            return Response::error($e->getMessage());
        }
    }
}
